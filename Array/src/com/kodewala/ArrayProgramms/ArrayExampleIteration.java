package com.kodewala.ArrayProgramms;

public class ArrayExampleIteration {

	public static void main(String[] args) {
		String [] names = new String [5];
		names [0] = "Abinash";
		names [1] = "Abinash";
		names [2] = "Abinash";
		names [3] = "Abinash";
		names [4] = "Abinash";
		
		for (int i = 0 ; i< names.length;i++) {
			System.out.println(names[i]);
		}
		
	}

}
